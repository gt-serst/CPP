#include "Weapon.hpp"
#include "HumanA.hpp"
#include "HumanB.hpp"

int	main(int argc, char **argv)
{
	{
		Weapon club = Weapon("crude spiked club");
		HumanA bob("Bob", club);
		bob.attack();
		club.setType("some other type of club");
		bob.attack();
	}
	{
		Weapon club = Weapon("crude spiked club");
		HumanB jim("Jim");
		jim.setWeapon(club);
		jim.attack();
		club.setType("some other type of club");
		jim.attack();
	}
	(void)argc;
	(void)argv;
	return (0);
}
